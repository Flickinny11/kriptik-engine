// Artifact 1: Experience Shell
// ---------------------------------------------------------------------------

/**
 * IExperienceShell — the app foundation built by the Design Pioneer.
 *
 * The Experience Shell is the landing page built using design_references
 * dependencies at maximum intensity. It renders on the integration server
 * within the first 5-10 minutes, establishing the visual quality floor
 * that all subsequent builder agents must maintain.
 *
 * Spec Section 7.2, Artifact 1 — "WebGL shader effects on hero elements,
 * GSAP + ScrollTrigger entrance animations, Lenis smooth scrolling,
 * custom cursor behavior, the full color/typography/spacing system,
 * page transitions, skeleton loading states, and responsive behavior."
 *
 * Spec Section 7.3, Layer 1 — "Dedicated agent creates Experience Shell
 * + components" as the visual quality floor.
 */
export interface IExperienceShell {
  /** Files that make up the Experience Shell. */
  readonly files: readonly IGeneratedFile[];
  /** Design tokens embedded in the shell. */
  readonly tokenSet: IDesignTokenSet;
  /** Effect names configured in the shell (e.g., "ShaderHover", "SmoothScroll"). */
  readonly effects: readonly string[];
  /** Layout system description (responsive breakpoints, grid configuration). */
  readonly layoutSystem: string;
  /** Whether the shell includes responsive behavior across breakpoints. */
  readonly responsive: boolean;
  /** When the shell was produced. */
  readonly createdAt: Date;
}

