  readonly session: import("./agents.js").ILiveAgentSession;
  /** Design brief from ICE Stage 2 (competitive intelligence). */
  readonly designBrief: import("./ice.js").IDesignBrief;
  /** Living specification from ICE. */
  readonly livingSpec: import("./ice.js").ILivingSpecification;
