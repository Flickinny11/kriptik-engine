/**
 * IDesignPioneerArtifacts — the complete output of the Design Pioneer's
 * early-build work.
 *
 * Contains all three artifacts plus metadata. The orchestrator uses this
 * to update the dependency graph (unblocking UI goals), inject component
 * references into builder goal assignments (Layer 2), and configure the
 * anti-slop linter with project-specific patterns.
 *
 * Spec Section 7.2 — all three artifacts described.
 * Spec Section 7.3, Layers 1-2 — Pioneer output feeds downstream enforcement.
 */
export interface IDesignPioneerArtifacts {
  /** Artifact 1: The Experience Shell. */
  readonly shell: IExperienceShell;
  /** Artifact 2: The Component Library. */
  readonly componentLibrary: IComponentLibrary;
  /** Artifact 3: The Anti-Slop Ruleset. */
  readonly antiSlopRuleset: IAntiSlopRuleset;
  /** All files generated across all three artifacts. */
  readonly allGeneratedFiles: readonly IGeneratedFile[];
  /** The build this was produced for. */
  readonly buildId: string;
  /** When artifacts were produced. */
  readonly producedAt: Date;
}

