/**
 * Design Pioneer agent interfaces — the specialized agent that establishes
 * the visual quality floor during early build by producing three artifacts:
 *   1. Experience Shell — the app foundation with design system tokens
 *   2. Component Library — project-specific reusable components
 *   3. Anti-Slop Ruleset — project-specific pattern enforcement
 *
 * Spec Section 7.1 — The Design Problem (why the Pioneer exists)
 * Spec Section 7.2 — The Design Pioneer (what it produces)
 * Spec Section 7.3 — Seven-Layer Design Enforcement Stack (Layer 1)
 */

// ---------------------------------------------------------------------------
// Design tokens — the atomic units of the design system
// ---------------------------------------------------------------------------

/**
 * A single design token — a named value in the design system.
 *
 * Tokens are expressed as CSS custom properties so all agents can reference
 * them without hardcoding values. Hardcoded values are caught by the
 * anti-slop linter (Layer 5).
 *
 * Spec Section 7.2, Artifact 1 — "the full color/typography/spacing system"
 * Spec Section 7.2, Artifact 2 — "tokens/ — colors, typography, spacing,
 * shadows, animations, gradients (all as CSS custom properties)"
 */
export interface IDesignToken {
  /** CSS custom property name (e.g., "--color-primary-500"). */
  readonly name: string;
  /** CSS value (e.g., "hsl(220, 90%, 56%)"). */
  readonly value: string;
  /** Token category (e.g., "color", "typography", "spacing"). */
  readonly category: string;
  /** Human-readable description of this token's purpose. */
  readonly description: string;
}

/**
 * The complete design token set — organized by category.
 *
 * All tokens are CSS custom properties. Builder agents reference these
 * instead of hardcoding values. The anti-slop linter's hardcoded-color
 * patterns enforce this at Layers 4-5.
 *
 * Spec Section 7.2, Artifact 2 — tokens/ directory structure.
 */
export interface IDesignTokenSet {
  readonly colors: readonly IDesignToken[];
  readonly typography: readonly IDesignToken[];
  readonly spacing: readonly IDesignToken[];
  readonly shadows: readonly IDesignToken[];
  readonly animations: readonly IDesignToken[];
  readonly gradients: readonly IDesignToken[];
}

// ---------------------------------------------------------------------------
// Generated files — output of the Design Pioneer's code generation
// ---------------------------------------------------------------------------

/**
 * A file generated by the Design Pioneer agent.
 *
 * The Pioneer uses filesystem tools to write these files to the repository.
 * This interface captures the structured output for artifact tracking
 * and dependency graph integration.
 */
export interface IGeneratedFile {
  /** File path relative to repository root. */
  readonly path: string;
  /** File content. */
  readonly content: string;
  /** Why this file exists — used in goal assignment context for builders. */
  readonly purpose: string;
}

