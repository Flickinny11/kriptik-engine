// =============================================================================
// Types
// =============================================================================

export type IntegrationCategory =
  | 'auth'
  | 'payments'
  | 'database'
  | 'storage'
  | 'email'
  | 'ai'
  | 'compute'
  | 'deployment'
  | 'analytics'
  | 'communication'
  | 'cms'
  | 'search'
  | 'monitoring'
  | 'scheduling'
  | 'maps'
  | 'social'
  | 'video'
  | 'forms'
  | 'crm'
  | 'ecommerce'
  | 'security'
  | 'other';

export interface CredentialField {
  /** Environment variable name */
  envVar: string;
  /** Human-readable label */
  label: string;
  /** Type of credential */
  type: 'api_key' | 'secret' | 'url' | 'token' | 'id' | 'generated_secret';
  /** Whether this is required */
  required: boolean;
  /** Whether user can click "Generate" for this (security secrets only) */
  canGenerate?: boolean;
  /** URL where user retrieves this credential */
  retrieveUrl?: string;
  /** Instructions for retrieving */
  retrieveInstructions?: string;
  /** Whether KripTik creates this after OAuth/API connection */
  kriptikGenerates?: boolean;
  /** Placeholder text */
  placeholder?: string;
}

export interface IntegrationMetadata {
  /** Unique identifier */
  id: string;
  /** Display name */
  name: string;
  /** Category for grouping */
  category: IntegrationCategory;
  /** Short description */
  description?: string;
  /** Whether OAuth is available via OAuth 150 native system */
  supportsOAuth: boolean;
  /** OAuth 150 provider ID (if OAuth supported) */
  oauthProviderId?: string;
  /** Official documentation URL */
  docsUrl: string;
  /** Where to get API keys (if not OAuth) */
  credentialRetrieveUrl?: string;
  /** Logo URL or slug for icon lookup */
  logoSlug?: string;
  /** Credentials user must provide */
  userProvides: CredentialField[];
  /** What KripTik generates after connection */
  kriptikGenerates: string[];
  /** Post-connection actions KripTik takes */
  postConnectionActions?: string[];
  /** Keywords for search/detection */
  keywords?: string[];
  /** Current API version */
  apiVersion?: string;
}

