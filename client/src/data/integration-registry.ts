/**
 * Get integration by ID
 */
export function getIntegrationById(id: string): IntegrationMetadata | undefined {
  return INTEGRATION_REGISTRY.find(i => i.id === id);
}

/**
 * Get integrations by category
 */
export function getIntegrationsByCategory(category: IntegrationCategory): IntegrationMetadata[] {
  return INTEGRATION_REGISTRY.filter(i => i.category === category);
}

/**
 * Search integrations by keyword
 */
export function searchIntegrations(query: string): IntegrationMetadata[] {
  const lowerQuery = query.toLowerCase();
  return INTEGRATION_REGISTRY.filter(i =>
    i.name.toLowerCase().includes(lowerQuery) ||
    i.id.toLowerCase().includes(lowerQuery) ||
    i.keywords?.some(k => k.toLowerCase().includes(lowerQuery)) ||
    i.description?.toLowerCase().includes(lowerQuery)
  );
}

/**
 * Get integrations that support OAuth
 */
export function getOAuthIntegrations(): IntegrationMetadata[] {
  return INTEGRATION_REGISTRY.filter(i => i.supportsOAuth);
}

/**
 * Get all categories with their integrations count
 */
export function getCategoryCounts(): Record<IntegrationCategory, number> {
  const counts = {} as Record<IntegrationCategory, number>;
  for (const category of Object.keys(CATEGORY_CONFIG) as IntegrationCategory[]) {
    counts[category] = INTEGRATION_REGISTRY.filter(i => i.category === category).length;
  }
  return counts;
}

export default INTEGRATION_REGISTRY;
